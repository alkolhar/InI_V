
public class Week {

	public String Monday(String name) {
		return name + " works on Monday.";
	}

	public String Tuesday(String name) {
		return name + " works on Tuesday too.";
	}

	public String Wednesday(String name) {
		return name + " also works on Wednesday.";
	}

	public String Thursday(String name) {
		return name + " even works on Thursday.";
	}

	public String Friday(String name) {
		return name + " works sometimes on Friday.";
	}

}
